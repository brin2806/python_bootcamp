
# Built in Functions

# str() converts any value to a string
str_example = str(5)
print(str_example)
print(type(str_example))

'''int() converts a number value (float or a string) to an integer.
For a float it drops the decimals.'''
int_example = int(2.56)
int_example_2 = int('2')
print(int_example)
print(int_example_2)
print(type(int_example))

# len() shows the number of characters in the string
length_example = len("Hello")
print(length_example)
print(type(length_example))

# type() gives the data type of the argument

# min() gives the smallest number or earliest alphabet
min_example = min(2,3,4,56)
print(min_example)

# max() gives the largest number or latest alphabet
max_example = max('a','g','z')
print(max_example)